function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value));
}

function clampRating(value: number): number {
  return Number(clamp(value, 3.6, 5).toFixed(1));
}

function buildReviewText(seed: Seed, highlight: string, idx: number): string {
  if (idx % 3 === 0) {
    return `${seed.name} delivered clear milestones and kept the scope aligned with the estimate. ${highlight} stood out most.`;
  }
  if (idx % 3 === 1) {
    return `Communication was consistent and the crew remained professional throughout the project. ${highlight} made the process easier.`;
  }
  return `We compared multiple bids and felt most confident in this team. ${highlight} helped us make an informed decision.`;
}

function buildReviewTags(seed: Seed): BusinessReview["tags"] {
  const tags: BusinessReview["tags"] = ["Clear communication", "Professional crew"];
  if (seed.quoteSpeed === "Same-day") tags.push("Responsive");
  if (seed.focus === "Commercial") tags.push("Commercial quality");
  else tags.push("On time");
  if (seed.reviewAverage >= 4.7) tags.push("Warranty honored");
  if (seed.specialties.some((item) => item.toLowerCase().includes("insurance"))) tags.push("Insurance support");
  return Array.from(new Set(tags)).slice(0, 4);
}

function buildReviews(seed: Seed, idxSeed: number): BusinessReview[] {
  return seed.reviewHighlights.map((highlight, idx) => {
    const rating = clampRating(seed.reviewAverage + (idx % 2 === 0 ? 0.1 : -0.2) + (idxSeed % 4 === 0 ? 0.1 : 0));
    return {
      id: `${seed.id}-review-${idx + 1}`,
      customerName: customerNames[(idxSeed + idx * 2) % customerNames.length],
      rating,
      date: `2025-${String(((idxSeed + idx) % 12) + 1).padStart(2, "0")}-${String(((idxSeed * 3 + idx * 5) % 26) + 1).padStart(2, "0")}`,
      text: buildReviewText(seed, highlight, idx),
      tags: buildReviewTags(seed),
    };
  });
}

function buildComplaints(seed: Seed): BusinessComplaint[] {
  const total = seed.complaintsClosed + seed.complaintsOpen;
  if (total === 0) return [];

  const entries = Math.min(4, Math.max(2, total > 8 ? 4 : total));
  return Array.from({ length: entries }).map((_, idx) => {
    const category = seed.complaintCategories[idx % seed.complaintCategories.length];
    const isOpen = idx < seed.complaintsOpen;
    const status = isOpen ? (idx % 2 === 0 ? "Open" : "Pending Business Reply") : idx % 2 === 0 ? "Closed" : "Resolved";

    return {
      id: `${seed.id}-complaint-${idx + 1}`,
      date: `2025-${String(((idx + 2) % 12) + 1).padStart(2, "0")}-${String(6 + idx * 6).padStart(2, "0")}`,
      category,
      summary: complaintSummaryByCategory[category],
      status,
      businessResponse: complaintResponseByCategory[category],
      outcomeTag: isOpen ? "Open" : idx % 2 === 0 ? "Resolved" : "Partially Resolved",
    };
  });
}

function resolutionRate(closed: number, open: number): number {
  const total = closed + open;
  if (total === 0) return 100;
  return Math.round((closed / total) * 100);
}

